#!/usr/bin/env node
// Reads every server/db/migrations/<name>/migration.sql and writes
// server/db/migrations/list.ts so Nitro's production Rollup bundle can import
// the SQL as plain TypeScript constants (import.meta.glob and useStorage both
// fail silently in Nitro's serverless bundle).
//
// Run automatically via:  npm run db:generate  and  npm run build (prebuild)

import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join, resolve } from 'node:path'

const migrationsDir = resolve('server/db/migrations')
const outputFile    = join(migrationsDir, 'list.ts')

const entries = readdirSync(migrationsDir, { withFileTypes: true })
  .filter(d => d.isDirectory())
  .map(d => d.name)
  .sort()
  .map(name => ({
    name,
    sql: readFileSync(join(migrationsDir, name, 'migration.sql'), 'utf-8'),
  }))

const lines = [
  '// AUTO-GENERATED by scripts/gen-migration-list.mjs — do not edit by hand.',
  '// Re-run via `npm run db:generate` or `npm run build`.',
  '',
  'export const MIGRATIONS: ReadonlyArray<{ name: string; sql: string }> = [',
  ...entries.map(({ name, sql }) =>
    `  { name: ${JSON.stringify(name)}, sql: ${JSON.stringify(sql)} },`
  ),
  ']',
  '',
]

writeFileSync(outputFile, lines.join('\n'))
console.log(`[gen-migration-list] ${entries.length} migration(s) → list.ts`)
